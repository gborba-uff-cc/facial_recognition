// JFIF modified Rec.601 with Y,Cb and Cr with full 8-bit range - working
        final cbAux = cbV-128;
        final crAux = crV-128;
        final r = (yV +1.402*(crAux))
          .round()
          .clamp(0, 255);
        final g = (yV -0.344136*(cbAux) -0.714136*(crAux))
          .round()
          .clamp(0, 255);
        final b = (yV +1.772*(cbAux))
          .round()
          .clamp(0, 255);
/**/
/*
