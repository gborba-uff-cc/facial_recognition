        final int r = ( (255/219)*(yV-16) +(255/224)*1.402*(crV-128) )
          .round()
          .clamp(0, 255);
        final int g = ( (255/219)*(yV-16) -(255/224)*1.772*(0.114/0.587)*(cbV-128) -(255/224)*1.402*(0.299/0.587)*(crV-128) )
